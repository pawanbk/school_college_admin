                                            <p><h2>Why New Zealand?</h2></p>

                                            <p>There are just over 4 million people who reside on the country, and it is one of the safest places to live in the world. Another cool thing about the people is that there are so many different kinds of people. For being such a small country, the diversity is greater than you see in many European countries.</p>

