                                            <p>There are plenty of work opportunities available for international students. On a student visa, you are allowed to work up to 20 hours a week through the semester; during vacations you can work up to 40 hours. So, instead of having to worry about finances, you get to supplement your education with income. You may even be able to nab internships and other practical work. The international studies office at your university can help you find a job to sustain you during your time in New Zealand. Another great thing is that you can get a permit at the end of your degree program and work for 12 months in the country under a special “work permit” that is alongside your student visa, which is what we’ll look at closer in the last point.</p>

                                            <p>The visa acquisition process is incredibly simple, and unlike many other countries, you will not be rejected right away. You get to talk to people and tell them your side of the story if there is something that may prevent you from getting a visa to come into the country. If you can explain why you’ve decided to go down there and your international studies program helps you to create a Statement of Purpose (a statement you give to the visa officer to tell them why you are coming to New Zealand to study), you will usually get a visa. The country is welcome to immigration, more so than almost any other country in the world. The last advantage is that, after you graduate, as mentioned above, you can work in the country for up to a year. If the job you are working at is related to the degree that you received, you can actually apply to get permanent residency, which you will most likely get within 6 months of your application. We look at visa acquisition and residency more in our Visa and Passport section.</p>
